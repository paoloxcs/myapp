			<p>Buscando compartir información técnica sobre sistemas de estanqueidad en diversas zonas del país, Casdel Hnos S.A. -empresa comercializadora de sistemas de sellados para maquinaria como orings, sellos hidráulicos, retenes radiales, así como fabricante de sellos especiales- realizará el “Taller de tecnología, aplicación y uso correcto en sellos para cilindros hidráulicos de la marca HALLITE”.</p>


			<p>Las reuniones, a realizarse en Lima y Trujillo, tendrán como objetivo difundir contenido teórico y práctico sobre sellos para cilindros hidráulicos a los jefes de mantenimiento de planta, logística, operarios de maquinaria y técnicos ubicados en nuestra capital y el norte del país. Estos temas se presentarán con diferentes sistemas en sellos que HALLITE comercializa en el Perú mediante Casdel. Estas ponencias estarán a cargo del gerente de ventas de Sudamérica de HALLITE, César Calderón, quien arribará desde California, Estados Unidos.</p>


			<p>En Lima, la capacitación será el martes 27 de noviembre en la planta de Casdel (Calle Rodolfo del Campo Nº 428 – La Victoria), en el horario de 6:00 p.m. a 10:00 p.m. En el caso de Trujillo, se realizará el 29 de noviembre en Hotel Presidente (Jirón Bolognesi Nº 368 – distrito de Trujillo), en el mismo horario.</p>


			<p>Cabe destacar que los cupos para estos talleres son limitados, por lo que es importante cumplir con el proceso de inscripción vía e-mail a informes@casdel.com.pe, con asunto “Taller Hallite”, indicando los datos completos de los asistentes. Al finalizar la capacitación se entregará un certificado sobre uso correcto y tecnología en sellos para cilindros hidráulicos a nombre de Casdel y HALLITE.</p>

